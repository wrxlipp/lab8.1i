#include <iostream>
#include <cstring>
using namespace std;

// Функція для підрахунку кількості символів "+", "-", "=" у рядку.
int CountOccurrences(const char* str) {
    int count = 0;  // Змінна для підрахунку кількості символів.
    for (int i = 0; str[i] != '\0'; ++i) {  // Перебір кожного символа рядка.
        if (str[i] == '+' || str[i] == '-' || str[i] == '=') {  // Перевірка, чи символ один із шуканих.
            ++count;  // Якщо знайдено, збільшуємо лічильник.
        }
    }
    return count;  // Повертаємо кількість знайдених символів.
}

// Функція для заміни кожного символу "+", "-", "=" на пару зірочок "**".
void ReplaceSymbols(char* str) {
    for (int i = 0; str[i] != '\0'; ++i) {  // Перебір кожного символа рядка.
        if (str[i] == '+' || str[i] == '-' || str[i] == '=') {  // Якщо символ один із шуканих.
            str[i] = '*';  // Заміна символа на '*'.
            for (int j = i + 1; str[j] != '\0'; ++j) {  // Перевірка наступного символа.
                str[j] = '*';  // Заміна наступного символа на '*'.
                break;  // Виходимо з циклу після заміни двох символів.
            }
        }
    }
}

int main() {
    char str[101];  // Оголошення масиву для введення рядка.
    cout << "Enter string:" << endl;  // Запит на введення рядка.
    cin.getline(str, 100);  // Введення рядка з обмеженням на 100 символів.
    cout << "Total occurrences of '+', '-', '=': " << CountOccurrences(str) << endl;  // Виведення кількості символів "+", "-", "=".
    ReplaceSymbols(str);  // Виклик функції для заміни символів.
    cout << "Modified string: " << str << endl;  // Виведення модифікованого рядка.
    return 0;  // Завершення програми.
}
